public class ComputeCircumference {
    public static void main(String[] args)
    {
        int radius1 = 1;
        int radius2 = 3;
        int radius3 = 5;

        final float PI = 3.14f;

        System.out.println(2*PI*radius1);
        System.out.println(2*PI*radius2);
        System.out.println(2*PI*radius3);
    }
}
