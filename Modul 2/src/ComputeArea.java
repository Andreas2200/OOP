public class ComputeArea {
    public static void main(String[] args)
    {
        int radius1 = 1;
        int radius2 = 3;
        int radius3 = 5;

        final float PI = 3.14f;

        System.out.println(""+radius1+" * " + "" + radius1 + " * " + PI + " is equal to " + radius1*radius1*PI);
        System.out.println(radius2*radius2*PI);
        System.out.println(radius3*radius3*PI);
    }
}
